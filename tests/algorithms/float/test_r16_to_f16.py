    for s0, s1 in R16_F16:
        x = bits("17h" + s0)
        x = R16(S=x[16], E=x[10:16], T=x[0:10])
        exp = bits("16h" + s1)
        exp = F16(S=exp[15], E=exp[10:15], T=exp[0:10])
        got = r2f(x)
        assert match(got, exp)
