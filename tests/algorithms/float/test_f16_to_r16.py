def test_f16_r16():
    for s0, s1 in F16_R16:
        x = bits("16h" + s0)
        x = F16(S=x[15], E=x[10:15], T=x[0:10])
        exp = bits("17h" + s1)
        exp = R16(S=exp[16], E=exp[10:16], T=exp[0:10])
        got = f2r(x)
        assert match(got, exp)
